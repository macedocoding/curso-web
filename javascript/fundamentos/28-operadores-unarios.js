let [a, b, c] = [1, 2, true];

console.log(!c); // Operador de negação inverte o valor lógico do operando
console.log(a++) // Operador pós fixado de incremento soma 1 ao operando APÓS quaisquer outras operações envolvendo-a
console.log(a--) // Similar ao exemplo anterior com decremento de 1 unidade
console.log(++a) // Operador pós fixado de incremento soma 1 ao operando ANTES de quaisquer outras operações envolvendo-a
console.log(--a) // Similar ao exemplo anterior com decremento de 1 unidade
